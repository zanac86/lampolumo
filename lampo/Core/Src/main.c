void init_my_devices() {
	HAL_Delay(300);
	OLED_Init(&hi2c1);
	OLED_FontSet(Font_MSX_6x8_rus1251);
	OLED_SetContrast(5);

	OLED_Clear(0);

	OLED_DrawStr("OLED Ok", 0, 0, 1);
	OLED_UpdateScreen();
	HAL_Delay(400);

	char str[20];
	sprintf(str, "S=%ld", HAL_RCC_GetSysClockFreq());
	OLED_DrawStr(str, 0, 16, 1);
	sprintf(str, "H=%ld", HAL_RCC_GetHCLKFreq());
	OLED_DrawStr(str, 64, 16, 1);
	sprintf(str, "1=%ld", HAL_RCC_GetPCLK1Freq());
	OLED_DrawStr(str, 0, 24, 1);
	sprintf(str, "2=%ld", HAL_RCC_GetPCLK2Freq());
	OLED_DrawStr(str, 64, 24, 1);
	OLED_UpdateScreen();

	HAL_ADCEx_Calibration_Start(&hadc1);

	HAL_Delay(1000);

}

void read_current_adc() {
	HAL_ADC_Start(&hadc1);
	HAL_ADC_PollForConversion(&hadc1, 100);
	u = HAL_ADC_GetValue(&hadc1);
	HAL_ADC_Stop(&hadc1);
}

void process_timer_event() {
	uint16_t tmp = ticks;
	HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET);

	OLED_DrawVLine(x, 0, 10);
	x = (x + 1) % OLED_WIDTH;
	if (x == 0) {
		OLED_Clear(0);
	}

	char str[15];
	sprintf(str, "%03d", x);
	OLED_DrawStr(str, 100, 48, 1);

	read_current_adc();

	sprintf(str, "%04d", u);
	OLED_DrawStr(str, 0, 48, 1);

	ticks_ = tmp - ticks_;
	sprintf(str, "%05d", ticks_);
	OLED_DrawStr(str, 32, 48, 1);
	ticks_ = tmp;

	OLED_UpdateScreen();

	HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET);
	ticks_=ticks;
}

void draw_waveform()
{
	if (signalInfo.decimated==0)
		return;
	OLED_Clear(0);
	for(uint16_t i=0;i<signalInfo.total_samples;i++)
	{
		OLED_DrawPixel(i, samples[i]);
	}
	OLED_UpdateScreen();
}

